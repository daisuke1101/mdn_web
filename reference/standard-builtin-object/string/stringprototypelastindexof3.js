var anyString = 'Brave new world';

console.log('The index of the 1st w from the beginning is ' + anyString.indexOf('w'));  // The index of the 1st w from the beginning is 8
console.log('The index of the 1st w from the end is ' + anyString.lastIndexOf('w'));  // The index of the 1st w from the end is 10
console.log('The index of "new" from the beginning is ' + anyString.indexOf('new'));  // The index of "new" from the beginning is 6
console.log('The index of "new" from the end is ' + anyString.lastIndexOf('new'));  // The index of "new" from the end is 6